package com.parkarcorp.iz.doorlistener;

import android.content.Intent;
import android.os.Bundle;
import android.view.Menu;
import android.view.MenuItem;
import android.widget.Button;

import butterknife.ButterKnife;
import butterknife.InjectView;
import butterknife.OnClick;
import utility.BaseActivity;

public class DashboardActivity extends BaseActivity {
    @InjectView(R.id.btnCheckBattery)
    Button btnCheckBattStatus;

    @InjectView(R.id.btnChangePhNo)
    Button btnChangePhoneNo;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_dashboard);

        ButterKnife.inject(this);

    }

    @OnClick(R.id.btnCheckBattery)
    public void CheckBatteryStatus() {
        final Intent intent = new Intent(this, RequestBatteryStatusActivity.class);
        startActivity(intent);
    }

    @OnClick(R.id.btnChangePhNo)
    public void changePhonNo() {
//        TODO : Change Call
        final Intent intent = new Intent(this, RequestBatteryStatusActivity.class);
        startActivity(intent);
    }


    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.dashboard, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();
        if (id == R.id.action_settings) {
            return true;
        }
        return super.onOptionsItemSelected(item);
    }
}
